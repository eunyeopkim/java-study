package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		// "Hello"
		System.out.println("\"Hello\"");
		
		// 제어문자
		// \t -> 탭
		// \n -> New Line(개행)
		// \r -> carriage Return
		System.out.print("Hello\tWorld\r\n");
		System.out.println("Hello\tWorld\n");
		
		char c = '\'';
		System.out.println(c);
		
		
	}

}
